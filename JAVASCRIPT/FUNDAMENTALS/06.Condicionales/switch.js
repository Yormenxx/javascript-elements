/* switch - case */
    
    // domingo -0
    // lunes - 1
    // martes - 2
    // miércoles - 3
    // jueves - 4
    // viernes - 5
    // sábado - 6
    
     let dia = 2;

    switch (dia) {
      case 0:
        console.log("Domingo");
        break;
      case 1:
        console.log("Lunes");
        break;
      case 2:
        console.log("Martes");
        break;
      case 3:
        console.log("Miércoles");
        break;
      case 4:
        console.log("Jueves");
        break;
      case 5:
        console.log("Viernes");
        break;
      case 6:
        console.log("Sábado");
        break;
      default:
        console.log("El día no existe");
        break;
    }
 
