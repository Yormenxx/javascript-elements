// La inferencia de tipos en TypeScript es la capacidad del compilador para determinar automáticamente el tipo de una variable, parámetro o valor de retorno sin que tú lo anotes explícitamente. 


let nombre = "Sofía"; // TypeScript infiere el tipo `string`
let edad = 28; // TypeScript infiere el tipo `number`
let isHuman = true;

console.log(typeof nombre);
console.log(typeof edad);
console.log(typeof isHuman);
